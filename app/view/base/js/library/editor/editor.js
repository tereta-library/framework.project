export class Editor {
    constructor(node) {
        this.node = node;
        node.editor = this;
    }

    show() {
        if (this.initialised) {
            this.renderLineBar(this.lineBar);
            return;
        }

        this.initialised = true;

        this.node.addEventListener('keyup', this.onKeyUp.bind(this))
        this.node.addEventListener('scroll', this.onScroll.bind(this))

        const element = this.node;
        let id = element.id;

        let editorWrapper = document.createElement("div");
        this.editorWrapper = editorWrapper;
        if (id) {
            editorWrapper.id = 'editorWrapper-' + id;
        }
        editorWrapper.classList.add('editorWrapper');

        let editorLayer = document.createElement("div");
        this.editorLayer = editorLayer;
        editorLayer.classList.add('editorLayer');

        let lineBar = document.createElement("div");
        this.lineBar = lineBar;
        lineBar.classList.add('editorLine');
        editorLayer.append(lineBar);

        editorWrapper.append(editorLayer);

        this.node.after(editorWrapper);
        editorWrapper.append(this.node);

        this.renderLineBar(lineBar);
    }

    onScroll(ev) {
        this.editorLayer.style.top = (this.node.scrollTop * -1) + 'px';

        this.update();
    }

    onKeyUp(ev) {
        this.breakLineAnalise(ev);
        this.update();
    }

    update() {
        this.renderLineBar(this.lineBar);
    }

    renderLineBar(lineBar = null) {
        const element = this.node;

        if (element.tagName.toLowerCase() != 'textarea') {
            console.error(`Editor can not be applied to non textarea element`, element);
            throw new Error(`Editor can not be applied to non textarea element`);
        }

        const stringLines = element.value.split("\n").length;
        let lineBarHtml = "";
        for (let f = 1; f < (stringLines + 1); f++) {
            lineBarHtml = lineBarHtml + `${f}<br />` + "\n";
        }

        lineBar.innerHTML = lineBarHtml;

        this.editorWrapper.style.paddingLeft = lineBar.offsetWidth + 'px';
    }

    breakLineAnalise(ev) {
        if (ev.key != 'Enter') return;
        const element = this.node;
        const selectionStart = element.selectionStart;
        const selectionEnd = element.selectionEnd;
        if (selectionStart != selectionEnd || selectionEnd == 0 || element.value.substring(selectionEnd - 1, selectionEnd) != "\n") return;

        const lastBefore = element.value.lastIndexOf("\n", selectionEnd - 2) + 1;
        const length = element.value.substring(lastBefore).search(/[^ \t]/);
        let text = element.value.slice(0, selectionStart) + (' '.repeat(length)) + element.value.slice(selectionEnd);
        element.value = text;
        element.selectionEnd = selectionEnd + length;
    }
}
